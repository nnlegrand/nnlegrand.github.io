/******************************************************************************
This is a demo of literals
*******************************************************************************/

#include <iostream>

using namespace std;

int main()
{
    cout << 12 << endl;             // this is an integer literal
    cout << 15.5 << endl;           // this is a double literal
    cout << 'A' << endl;            // this is a character literal
    cout << "ABCD" << endl;         // this is a string literal
    cout << true << endl;           // this is a boolean literal

    return 0;
}
